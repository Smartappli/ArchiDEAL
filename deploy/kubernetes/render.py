def fail(message: str) -> None:
    raise ValueError(message)


def is_dns_hostname(value: str) -> bool:
    if not HOST.fullmatch(value):
        return False
    try:
        ipaddress.ip_address(value)
    except ValueError:
        return True
    return False


def _config_csv(runtime_config: dict, key: str) -> tuple[str, ...]:
    raw_value = runtime_config.get(key, "")
    if not isinstance(raw_value, str):
        fail(f"{key} must be a comma-separated string.")
    if not raw_value.strip():
        return ()
    raw_items = raw_value.split(",")
    items = tuple(item.strip() for item in raw_items)
    if any(not item for item in items):
        fail(f"{key} must not contain empty entries.")
    return items


def _is_internal_dns_name(value: str) -> bool:
    if (
        not value
        or len(value) > 253
        or value.endswith(".")
        or value == "localhost"
        or value.endswith(".localhost")
        or any(DNS_LABEL.fullmatch(label) is None for label in value.split("."))
    ):
        return False
    try:
        ipaddress.ip_address(value)
    except ValueError:
        return not value.replace(".", "").isdigit()
    return False


def _is_runtime_upstream_host(value: str) -> bool:
    labels = value.casefold().split(".")
    return (
        RUNTIME_APPS_NAMESPACE in labels
        or bool(labels and labels[0].startswith("dealrt-"))
    )


def _network_policy_pod_names(peer: dict) -> set[str]:
    selector = peer.get("podSelector", {})
    names: set[str] = set()
    label_name = selector.get("matchLabels", {}).get("app.kubernetes.io/name")
    if isinstance(label_name, str):
        names.add(label_name)
    for expression in selector.get("matchExpressions", []):
        if (
            expression.get("key") == "app.kubernetes.io/name"
            and expression.get("operator") == "In"
        ):
            names.update(
                value
                for value in expression.get("values", [])
                if isinstance(value, str)
            )
    return names


def validate_apisix_route_policy(runtime_config: dict, documents: list[dict]) -> None:
    hosts = _config_csv(runtime_config, "APISIX_ROUTE_ALLOWED_UPSTREAM_HOSTS")
    raw_suffixes = _config_csv(
        runtime_config,
        "APISIX_ROUTE_ALLOWED_UPSTREAM_SUFFIXES",
    )
    ports = _config_csv(runtime_config, "APISIX_ROUTE_ALLOWED_UPSTREAM_PORTS")
    upstreams = _config_csv(runtime_config, "APISIX_ROUTE_ALLOWED_UPSTREAMS")
    if not (hosts or raw_suffixes) or not ports or not upstreams:
        fail(
            "APISIX dynamic routes require a non-empty upstream host or suffix "
            "allowlist, a non-empty port allowlist and a non-empty exact "
            "host:port allowlist."
        )

    if any(not _is_internal_dns_name(host) for host in hosts):
        fail(
            "APISIX_ROUTE_ALLOWED_UPSTREAM_HOSTS must contain only strict DNS "
            "hostnames, never IP addresses or localhost."
        )
    suffixes = tuple(suffix.removeprefix(".") for suffix in raw_suffixes)
    if any(not _is_internal_dns_name(suffix) for suffix in suffixes):
        fail(
            "APISIX_ROUTE_ALLOWED_UPSTREAM_SUFFIXES must contain only strict DNS "
            "suffixes, never IP addresses or localhost."
        )
    runtime_sentinel = (
        f"dealrt-sentinel.{RUNTIME_APPS_NAMESPACE}.svc.cluster.local"
    )
    if any(_is_runtime_upstream_host(host) for host in hosts) or any(
        runtime_sentinel == suffix or runtime_sentinel.endswith(f".{suffix}")
        for suffix in suffixes
    ):
        fail(
            "APISIX upstream allowlists must not include the runtime-apps namespace "
            "while runtime exposure is unsupported."
        )

    parsed_ports: list[int] = []
    for raw_port in ports:
        if not raw_port.isascii() or not raw_port.isdigit():
            fail("APISIX_ROUTE_ALLOWED_UPSTREAM_PORTS must contain only numeric ports.")
        port = int(raw_port)
        if raw_port != str(port) or port < 1 or port > 65535:
            fail(
                "APISIX_ROUTE_ALLOWED_UPSTREAM_PORTS must contain canonical ports "
                "between 1 and 65535."
            )
        parsed_ports.append(port)
    if len(set(parsed_ports)) != len(parsed_ports):
        fail("APISIX_ROUTE_ALLOWED_UPSTREAM_PORTS must not contain duplicates.")

    allowed_hosts = set(hosts)
    allowed_suffixes = set(suffixes)
    exact_pairs: set[tuple[str, int]] = set()
    for upstream in upstreams:
        host, separator, raw_port = upstream.rpartition(":")
        if (
            not separator
            or not _is_internal_dns_name(host)
            or not raw_port.isascii()
            or not raw_port.isdigit()
        ):
            fail(
                "APISIX_ROUTE_ALLOWED_UPSTREAMS must contain only canonical "
                "DNS host:port pairs."
            )
        port = int(raw_port)
        if raw_port != str(port) or port < 1 or port > 65535:
            fail(
                "APISIX_ROUTE_ALLOWED_UPSTREAMS must contain only canonical "
                "DNS host:port pairs."
            )
        host_is_allowed = host in allowed_hosts or any(
            host == suffix or host.endswith(f".{suffix}") for suffix in allowed_suffixes
        )
        if not host_is_allowed or port not in parsed_ports:
            fail(
                "Every APISIX exact upstream must also belong to the host/suffix "
                "and port allowlists."
            )
        pair = (host, port)
        if _is_runtime_upstream_host(host):
            fail(
                "APISIX exact upstreams must not target runtime-app Services while "
                "runtime exposure is unsupported."
            )
        if pair in exact_pairs:
            fail("APISIX_ROUTE_ALLOWED_UPSTREAMS must not contain duplicates.")
        exact_pairs.add(pair)

    bootstrap_config = next(
        (
            document
            for document in documents
            if document.get("kind") == "ConfigMap"
            and document.get("metadata", {}).get("name") == "apisix-bootstrap"
        ),
        None,
    )
    try:
        bootstrap = json.loads(bootstrap_config["data"]["routes.json"])
        bootstrap_routes = bootstrap["routes"]
    except (KeyError, TypeError, json.JSONDecodeError) as exc:
        fail(f"APISIX bootstrap routes are not valid JSON: {exc}")
    if not isinstance(bootstrap_routes, list):
        fail("APISIX bootstrap routes must be a JSON list.")
    bootstrap_route_ids = [
        route.get("id") if isinstance(route, dict) else None
        for route in bootstrap_routes
    ]
    if (
        len(bootstrap_route_ids) != len(EXPECTED_APISIX_BOOTSTRAP_UPSTREAMS)
        or set(bootstrap_route_ids) != set(EXPECTED_APISIX_BOOTSTRAP_UPSTREAMS)
    ):
        fail(
            "APISIX bootstrap must contain exactly the reviewed core routes; "
            "runtime routes are forbidden."
        )

    bootstrap_pairs: set[tuple[str, int]] = set()
    oidc_upstreams = {
        "dealhost",
        "dealiot",
        "dealdata-core",
        "dealdata-gps",
        "dealdata-sensor",
    }
    for route in bootstrap_routes:
        try:
            route_id = route["id"]
            nodes = route["upstream"]["nodes"]
            header_policy = route["plugins"]["proxy-rewrite"]["headers"]
        except (KeyError, TypeError) as exc:
            fail(f"APISIX bootstrap route contract is incomplete: {exc}")
        if not isinstance(nodes, dict) or len(nodes) != 1:
            fail("Every APISIX bootstrap route must have exactly one upstream node.")
        if not isinstance(header_policy, dict):
            fail("Every APISIX bootstrap route must define a header policy.")
        node = next(iter(nodes))
        host, separator, raw_port = node.rpartition(":")
        if not separator or not raw_port.isdigit():
            fail("APISIX bootstrap upstream nodes must use DNS host:port syntax.")
        pair = (host, int(raw_port))
        if pair != EXPECTED_APISIX_BOOTSTRAP_UPSTREAMS[route_id]:
            fail(
                "APISIX bootstrap routes must target only their reviewed core "
                "upstreams; runtime upstreams are forbidden."
            )
        if route_id != "archideal-interface":
            bootstrap_pairs.add(pair)

        set_headers = header_policy.get("set", {})
        removed_headers = set(header_policy.get("remove", []))
        if set_headers.get("X-Forwarded-Proto") != "$http_x_forwarded_proto":
            fail("Every APISIX route must preserve the trusted edge scheme.")
        if host in oidc_upstreams:
            if (
                set_headers.get("Authorization")
                != ("Bearer $http_x_forwarded_access_token")
                or "X-Forwarded-Access-Token" not in removed_headers
                or "Authorization" in removed_headers
            ):
                fail(
                    "Every OIDC-introspecting route must exchange the forwarded "
                    "token for Authorization and remove the raw token header."
                )
        elif (
            "Authorization" in set_headers
            or not {"Authorization", "X-Forwarded-Access-Token"} <= removed_headers
        ):
            fail(
                "Non-introspecting APISIX upstreams must strip Authorization and "
                "X-Forwarded-Access-Token."
            )

    if exact_pairs != bootstrap_pairs:
        fail(
            "APISIX_ROUTE_ALLOWED_UPSTREAMS must exactly match the deployed "
            "non-interface bootstrap upstreams."
        )

    services = {
        document.get("metadata", {}).get("name"): document
        for document in documents
        if document.get("kind") == "Service"
    }
    apisix_egress = next(
        (
            document
            for document in documents
            if document.get("kind") == "NetworkPolicy"
            and document.get("metadata", {}).get("name") == "apisix-egress"
        ),
        None,
    )
    if apisix_egress is None:
        fail("The APISIX egress NetworkPolicy is required.")
    network_pairs: set[tuple[str, int]] = set()
    for rule in apisix_egress.get("spec", {}).get("egress", []):
        pod_names: set[str] = set()
        for peer in rule.get("to", []):
            pod_names.update(_network_policy_pod_names(peer))
        rule_ports = {
            port.get("port")
            for port in rule.get("ports", [])
            if port.get("protocol", "TCP") == "TCP"
            and isinstance(port.get("port"), int)
        }
        network_pairs.update(
            (pod_name, port) for pod_name in pod_names for port in rule_ports
        )

    for host, port in exact_pairs:
        service = services.get(host)
        if service is None:
            fail(f"APISIX exact upstream Service is not deployed: {host}")
        service_ports = {
            item.get("port") for item in service.get("spec", {}).get("ports", [])
        }
        pod_name = (
            service.get("spec", {}).get("selector", {}).get("app.kubernetes.io/name")
        )
        if port not in service_ports or (pod_name, port) not in network_pairs:
            fail(
                "APISIX exact upstreams must match deployed Service ports and "
                f"apisix-egress NetworkPolicy rules: {host}:{port}"
            )


def validate_render_scalar(key: str, value: str) -> None:
    """Reject characters that could escape a quoted YAML substitution context."""
    if any(
        ord(character) < 0x21 or ord(character) > 0x7E or character in {'"', "'", "\\"}
        for character in value
    ):
        fail(
            f"{key} must use visible ASCII without whitespace, control characters, "
            "quotes or backslashes."
        )


def parse_https_endpoint(key: str, value: str) -> tuple[SplitResult, int]:
    """Parse a canonical HTTPS endpoint after the YAML-safety gate."""
    try:
        parsed = urlsplit(value)
        hostname = parsed.hostname
        port = parsed.port
    except ValueError as exc:
        fail(f"{key} is not a valid HTTPS URL: {exc}")

    if (
        parsed.scheme != "https"
        or not hostname
        or not is_dns_hostname(hostname)
        or parsed.username is not None
        or parsed.password is not None
        or parsed.fragment
        or parsed.query
    ):
        fail(
            f"{key} must be an HTTPS URL with a DNS hostname and without "
            "credentials, query or fragment."
        )

    expected_authority = hostname if port is None else f"{hostname}:{port}"
    if parsed.netloc != expected_authority:
        fail(f"{key} must use a canonical DNS hostname and numeric port.")
    if not SAFE_URL_PATH.fullmatch(parsed.path) or re.search(
        r"%(?![0-9A-Fa-f]{2})", parsed.path
    ):
        fail(f"{key} contains an invalid URL path.")

    return parsed, 443 if port is None else port


def load_values(path: Path, *, allow_example: bool) -> dict[str, str]:
    raw = yaml.load(path.read_text(encoding="utf-8"), Loader=UniqueKeyLoader)
    if not isinstance(raw, dict):
        fail("Values must be a flat YAML mapping.")
    if not all(isinstance(key, str) for key in raw):
        fail("Values keys must be strings.")
    if set(raw) != REQUIRED:
        missing = sorted(REQUIRED - set(raw))
        extra = sorted(set(raw) - REQUIRED)
        fail(f"Values keys mismatch; missing={missing}, extra={extra}")
    values: dict[str, str] = {}
    for key, value in raw.items():
        if not isinstance(value, str) or not value:
            fail(f"{key} must be a non-empty string.")
        validate_render_scalar(key, value)
        values[key] = value

    if not RELEASE.fullmatch(values["RELEASE_ID"]):
        fail("RELEASE_ID must be a lowercase DNS label.")
    if values["RELEASE_ID"] in {"none", "pending"}:
        fail("RELEASE_ID uses a reserved promotion-state sentinel.")
    if len(values["RELEASE_ID"]) > 39:
        fail(
            "RELEASE_ID must be at most 39 characters so every rendered "
            "Kubernetes Job name remains a valid DNS label."
        )
    for key in (
        "INGRESS_CLASS",
        "INGRESS_NAMESPACE",
        "TLS_SECRET_NAME",
        "CLUSTER_ISSUER",
        "SECRET_STORE_NAME",
        "MONITORING_NAMESPACE",
    ):
        value = values[key]
        if len(value) > 253 or not all(
            DNS_LABEL.fullmatch(label) for label in value.split(".")
        ):
            fail(f"{key} must be a DNS-1123 subdomain.")
    if (
        values["INGRESS_NAMESPACE"] == "archideal"
        or values["MONITORING_NAMESPACE"] == "archideal"
        or values["INGRESS_NAMESPACE"] == values["MONITORING_NAMESPACE"]
    ):
        fail(
            "INGRESS_NAMESPACE, MONITORING_NAMESPACE and the archideal application "
            "namespace must be distinct trust boundaries."
        )
    for key in (
        "PUBLIC_HOST",
        "MQTT_HOST",
        "POSTGRES_METADATA_HOST",
        "POSTGRES_DATA_HOST",
        "POSTGRES_DEALIOT_REGISTRY_HOST",
        "VALKEY_HOST",
        "ETCD_TLS_SERVER_NAME",
    ):
        if not is_dns_hostname(values[key]):
            fail(f"{key} must be a DNS hostname, without scheme or wildcard.")
    networks: dict[str, ipaddress.IPv4Network | ipaddress.IPv6Network] = {}
    for key in (
        "KAFKA_EGRESS_CIDR",
        "MQTT_EGRESS_CIDR",
        "POSTGRES_METADATA_EGRESS_CIDR",
        "POSTGRES_DATA_EGRESS_CIDR",
        "POSTGRES_DEALIOT_REGISTRY_EGRESS_CIDR",
        "KUBERNETES_API_EGRESS_CIDR",
        "VALKEY_EGRESS_CIDR",
        "ETCD_EGRESS_CIDR",
        "POD_CIDR",
        "INGRESS_PROXY_CIDR",
        "OIDC_EGRESS_CIDR",
        "GITHUB_EGRESS_CIDR",
        "OTEL_EGRESS_CIDR",
    ):
        network = ipaddress.ip_network(values[key], strict=True)
        if network.prefixlen == 0:
            fail(f"{key} must not permit the whole Internet.")
        networks[key] = network
    private_dependency_keys = (
        "KAFKA_EGRESS_CIDR",
        "MQTT_EGRESS_CIDR",
        "POSTGRES_METADATA_EGRESS_CIDR",
        "POSTGRES_DATA_EGRESS_CIDR",
        "POSTGRES_DEALIOT_REGISTRY_EGRESS_CIDR",
        "KUBERNETES_API_EGRESS_CIDR",
        "VALKEY_EGRESS_CIDR",
        "ETCD_EGRESS_CIDR",
    )
    for key in (*private_dependency_keys, "POD_CIDR", "INGRESS_PROXY_CIDR"):
        network = networks[key]
        if network.version != 4 or not any(
            network.subnet_of(private_network) for private_network in RFC1918_NETWORKS
        ):
            fail(f"{key} must be an IPv4 RFC1918 network.")
    for index, key in enumerate(private_dependency_keys):
        if networks[key].overlaps(networks["POD_CIDR"]):
            fail(f"{key} must not overlap POD_CIDR.")
        for other_key in private_dependency_keys[index + 1 :]:
            if networks[key].overlaps(networks[other_key]):
                fail(f"{key} must not overlap {other_key}.")
    if networks["KUBERNETES_API_EGRESS_CIDR"].prefixlen != 32:
        fail("KUBERNETES_API_EGRESS_CIDR must identify exactly one IPv4 API endpoint.")
    for key in ("OIDC_EGRESS_CIDR", "GITHUB_EGRESS_CIDR", "OTEL_EGRESS_CIDR"):
        network = networks[key]
        minimum_prefix = 20 if network.version == 4 else 48
        if network.prefixlen < minimum_prefix:
            fail(
                f"{key} must be a change-reviewed provider range no broader than "
                f"/{minimum_prefix} for IPv{network.version}."
            )
        if networks[key].overlaps(networks["POD_CIDR"]):
            fail(f"{key} must not overlap POD_CIDR.")
    parsed_https_urls: dict[str, tuple[SplitResult, int]] = {}
    for key in (
        "OIDC_ISSUER_URL",
        "OIDC_INTROSPECTION_URL",
        "OTEL_COLLECTOR_HTTP_ENDPOINT",
        "ETCD_ENDPOINT_1",
        "ETCD_ENDPOINT_2",
        "ETCD_ENDPOINT_3",
    ):
        parsed_https_urls[key] = parse_https_endpoint(key, values[key])

    for index in range(1, 4):
        key = f"ETCD_ENDPOINT_{index}"
        if parsed_https_urls[key][0].path not in {"", "/"}:
            fail(f"{key} must not contain a path.")

    for key in ("OIDC_ISSUER_URL", "OIDC_INTROSPECTION_URL"):
        if parsed_https_urls[key][1] != 443:
            fail(f"{key} must use TCP port 443 allowed by the OIDC NetworkPolicies.")
    if parsed_https_urls["OTEL_COLLECTOR_HTTP_ENDPOINT"][1] != 443:
        fail(
            "OTEL_COLLECTOR_HTTP_ENDPOINT must use TCP port 443 allowed by the "
            "APISIX NetworkPolicy."
        )
    for index in range(1, 4):
        key = f"ETCD_ENDPOINT_{index}"
        if parsed_https_urls[key][1] != 2379:
            fail(f"{key} must use TCP port 2379 allowed by the APISIX NetworkPolicy.")
    brokers = values["KAFKA_BOOTSTRAP_SERVERS"].split(",")
    if len(set(brokers)) < 3:
        fail("KAFKA_BOOTSTRAP_SERVERS must list at least three brokers.")
    broker_hosts: set[str] = set()
    for broker in brokers:
        host, separator, port = broker.rpartition(":")
        if not separator or not is_dns_hostname(host) or not port.isdigit():
            fail("Each Kafka bootstrap server must be a DNS hostname and numeric port.")
        if int(port) != 9093:
            fail(
                "Each Kafka bootstrap server must use TCP port 9093 allowed by "
                "the runtime NetworkPolicy."
            )
        broker_hosts.add(host)
    if len(broker_hosts) < 3:
        fail("KAFKA_BOOTSTRAP_SERVERS must use at least three distinct hostnames.")
    etcd_urls = [values[f"ETCD_ENDPOINT_{index}"] for index in range(1, 4)]
    etcd_hosts = {
        parsed_https_urls[f"ETCD_ENDPOINT_{index}"][0].hostname for index in range(1, 4)
    }
    if len(set(etcd_urls)) != 3 or len(etcd_hosts) != 3:
        fail("The three etcd endpoints must use three distinct URLs and hostnames.")
    for key in ("OIDC_ALLOWED_GROUP", "OIDC_ADMIN_GROUP", "OIDC_AUDIENCE"):
        if not SAFE_IDENTIFIER.fullmatch(values[key]) or ".." in values[key]:
            fail(f"{key} contains unsafe characters.")
    if values["OIDC_ADMIN_GROUP"] == values["OIDC_ALLOWED_GROUP"]:
        fail("OIDC_ADMIN_GROUP must be distinct from OIDC_ALLOWED_GROUP.")
    secret_prefix = values["SECRET_PREFIX"]
    if not SAFE_SECRET_PREFIX.fullmatch(secret_prefix) or ".." in secret_prefix:
        fail("SECRET_PREFIX contains unsafe characters.")
    image_repositories = load_image_repository_policy()
    for key in sorted(name for name in REQUIRED if name.startswith("IMAGE_")):
        match = IMAGE.fullmatch(values[key])
        if not match:
            fail(f"{key} must be an image reference pinned by sha256 digest.")
        if values[key].rsplit("@sha256:", 1)[0] != image_repositories[key]:
            fail(f"{key} must use the repository approved by supply-chain policy.")
        if not allow_example and match.group(1) == "0" * 64:
            fail(f"{key} still has the example all-zero digest.")

    if not allow_example:
        example_markers = (".invalid", "replace/", "example", "REPLACE")
        for key, value in values.items():
            if any(marker.lower() in value.lower() for marker in example_markers):
                fail(f"{key} still contains an example marker.")
    return values


def render_text(text: str, values: dict[str, str], path: Path) -> str:
    used = set(PLACEHOLDER.findall(text))
    unknown = used - set(values)
    if unknown:
        fail(f"{path}: unknown placeholders {sorted(unknown)}")
    rendered = PLACEHOLDER.sub(lambda match: values[match.group(1)], text)
    unresolved = PLACEHOLDER.findall(rendered)
    if unresolved:
        fail(f"{path}: unresolved placeholders {sorted(set(unresolved))}")
    return rendered


def validate_yaml_tree(root: Path) -> list[dict]:
    rendered_documents: list[dict] = []
    for path in sorted((*root.rglob("*.yaml"), *root.rglob("*.yml"))):
        try:
            documents = list(
                yaml.load_all(
                    path.read_text(encoding="utf-8"),
                    Loader=UniqueKeyLoader,
                )
            )
        except yaml.YAMLError as exc:
            fail(f"Invalid rendered YAML at {path}: {exc}")
        if not any(document is not None for document in documents):
            fail(f"Rendered YAML is empty: {path}")
        rendered_documents.extend(
            document for document in documents if isinstance(document, dict)
        )
    return rendered_documents


def validate_runtime_apps_fail_closed(
    runtime_documents: list[dict],
    all_documents: list[dict],
) -> None:
    """Reject every static ingress path into the managed-application namespace."""

    for document in runtime_documents:
        kind = document.get("kind")
        name = document.get("metadata", {}).get("name", "<unnamed>")
        if kind in RUNTIME_EXPOSURE_KINDS:
            fail(
                f"Runtime-apps must not contain {kind} resources while exposure "
                f"is unsupported: {name}."
            )
        if kind != "Service":
            continue
        spec = document.get("spec")
        if not isinstance(spec, dict) or spec.get("type") != "ClusterIP":
            fail(
                "Runtime-app Services must declare type ClusterIP; NodePort, "
                f"LoadBalancer and ExternalName are forbidden: {name}."
            )
        externally_routable_fields = {
            "allocateLoadBalancerNodePorts",
            "externalIPs",
            "externalName",
            "externalTrafficPolicy",
            "healthCheckNodePort",
            "loadBalancerClass",
            "loadBalancerIP",
            "loadBalancerSourceRanges",
        }
        ports = spec.get("ports", [])
        if (
            externally_routable_fields & set(spec)
            or not isinstance(ports, list)
            or any(
                not isinstance(port, dict) or "nodePort" in port for port in ports
            )
        ):
            fail(
                "Runtime-app ClusterIP Services must not declare any external or "
                f"node port: {name}."
            )

    tree_policies = [
        document
        for document in runtime_documents
        if document.get("kind") == "NetworkPolicy"
    ]
    cluster_runtime_policies = [
        document
        for document in all_documents
        if document.get("kind") == "NetworkPolicy"
        and document.get("metadata", {}).get("namespace")
        == RUNTIME_APPS_NAMESPACE
    ]
    for policies in (tree_policies, cluster_runtime_policies):
        names = [policy.get("metadata", {}).get("name") for policy in policies]
        if (
            len(policies) != len(RUNTIME_APPS_NETWORK_POLICIES)
            or set(names) != RUNTIME_APPS_NETWORK_POLICIES
        ):
            fail(
                "Runtime-apps NetworkPolicies must be exactly the default-deny "
                "and DNS-only baseline; additional policies are forbidden."
            )

    policies_by_name = {
        policy["metadata"]["name"]: policy for policy in tree_policies
    }
    expected_default_deny = {
        "podSelector": {},
        "policyTypes": ["Ingress", "Egress"],
    }
    expected_dns_only = {
        "podSelector": {},
        "policyTypes": ["Egress"],
        "egress": [
            {
                "to": [
                    {
                        "namespaceSelector": {
                            "matchLabels": {
                                "kubernetes.io/metadata.name": "kube-system"
                            }
                        },
                        "podSelector": {
                            "matchExpressions": [
                                {
                                    "key": "k8s-app",
                                    "operator": "In",
                                    "values": ["kube-dns", "coredns"],
                                }
                            ]
                        },
                    }
                ],
                "ports": [
                    {"protocol": "UDP", "port": 53},
                    {"protocol": "TCP", "port": 53},
                ],
            }
        ],
    }
    default_deny = policies_by_name["runtime-apps-default-deny"]
    dns_only = policies_by_name["runtime-apps-allow-dns-egress"]
    if (
        default_deny.get("metadata", {}).get("namespace")
        != RUNTIME_APPS_NAMESPACE
        or default_deny.get("spec") != expected_default_deny
        or dns_only.get("metadata", {}).get("namespace")
        != RUNTIME_APPS_NAMESPACE
        or dns_only.get("spec") != expected_dns_only
    ):
        fail(
            "Runtime-apps must keep an exact deny-all ingress/egress policy and "
            "the reviewed DNS-only egress exception."
        )


